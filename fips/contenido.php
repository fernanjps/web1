        <div class="section">
            <h3>2. Tipos de Suelo Ideales para Jardines</h3>
            <p>El suelo limoso es ideal para la mayoría de las plantas de jardín, ya que proporciona un buen equilibrio entre retención de agua y drenaje. Este tipo de suelo contiene una mezcla de arena, limo y arcilla.</p>
            <img src=" images/tiposuelo.png " alt="Tipos de suelo">
            <p>Para más detalles, mira este <a href="https://www.youtube.com/watch?v=example2" target="_blank">video sobre cómo preparar el suelo para el jardín</a>.</p>
        </div>

        <div class="section">
            <h3>3. Control de Plagas en el Jardín</h3>
            <p>Los pulgones son una plaga común que puede ser controlada eficazmente con jabón insecticida diluido. Este método es seguro para las plantas y ayuda a mantenerlas libres de estos pequeños insectos.</p>
            <img src="images/aphids.png" alt="Pulgones">
            <p>Para aprender más, puedes ver este <a href="https://www.youtube.com/watch?v=example3" target="_blank">video sobre control de pulgones</a>.</p>
        </div>

        <div class="section">
            <h3>4. Época de Poda de Árboles y Arbustos</h3>
            <p>La mayoría de los árboles y arbustos se deben podar en invierno, cuando las plantas están inactivas. Esto permite que las plantas se recuperen y crezcan con más vigor en la primavera.</p>
            <img src=" images/poda.png " alt="Poda de árboles">
            <p>Más información en este <a href="https://www.youtube.com/watch?v=example4" target="_blank">video sobre la poda de invierno</a>.</p>
        </div>

        <div class="section">
            <h3>5. Métodos de Jardinería Orgánica</h3>
            <p>El uso de fertilizantes químicos de liberación lenta no es un método de jardinería orgánica. En cambio, la adición de compost y la rotación de cultivos son prácticas que mejoran la salud del suelo de manera natural.</p>
            <img src="images/jardiorga.png  " alt="Jardinería orgánica">
            <p>Para más detalles, puedes ver este <a href="https://www.youtube.com/watch?v=example5" target="_blank">video sobre jardinería orgánica</a>.</p>
        </div>

        <div class="section">
            <h3>6. Plantas para Condiciones de Poca Luz</h3>
            <p>La planta de serpiente (Sansevieria) es ideal para crecer en condiciones de poca luz. Es fácil de cuidar y tolera bien la falta de luz directa.</p>
            <img src="images/pserpiente.png" alt="Planta de serpiente">
            <p>Más información en este <a href="https://www.youtube.com/watch?v=example6" target="_blank">video sobre plantas de interior para poca luz</a>.</p>
        </div>

        <div class="section">
            <h3>7. pH Ideal del Suelo</h3>
            <p>El pH ideal del suelo para la mayoría de las plantas de jardín es entre 6.5 y 7.5. Este rango permite que los nutrientes estén disponibles para las plantas.</p>
            <img src="images/phsuelo.png" alt="pH del suelo">
            <p>Para aprender más, puedes ver este <a href="https://www.youtube.com/watch?v=example7" target="_blank">video sobre cómo medir y ajustar el pH del suelo</a>.</p>
        </div>

        <div class="section">
            <h3>8. Métodos de Riego Eficientes</h3>
            <p>El riego por goteo es uno de los métodos más eficientes en términos de conservación de agua. Este sistema entrega agua directamente a las raíces de las plantas, minimizando la evaporación y el desperdicio.</p>
            <img src=" images/riegog.png " alt="Riego por goteo">
